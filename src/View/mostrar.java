    public void readAlunosTable (){
        setTitulos("Alunos","Matricula","Nome");
        DefaultTableModel modelo = (DefaultTableModel) mostrarTable.getModel();
        modelo.setNumRows(0);
        aDAO.read().forEach((a) -> {
            modelo.addRow(new Object[]{
                a.getMatricula(),
                a.getNomeAluno()
            });
        });
    }
    
